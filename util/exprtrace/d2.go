package exprtrace

import (
	"crypto/sha1"
	"encoding/hex"
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"sort"
	"strconv"
	"strings"
	"time"
)

// StackFrame is one parsed frame from runtime.Stack output.
type StackFrame struct {
	Function string
	File     string
	Line     int
}

// captureStack returns frames from the current goroutine's stack, outermost
// (main / goroutine entry) first. Frames inside the exprtrace package, the
// runtime, and the expr-lang internals are filtered out so the result reads
// as "how did we get here from operate()".
func captureStack() []StackFrame {
	var buf [1 << 14]byte
	n := runtime.Stack(buf[:], false)
	raw := parseGoStack(string(buf[:n]))
	filtered := raw[:0]
	for _, f := range raw {
		if strings.Contains(f.Function, "argo-workflows/v4/util/exprtrace") {
			continue
		}
		if strings.HasPrefix(f.Function, "runtime.") {
			continue
		}
		if strings.Contains(f.Function, "expr-lang/expr") {
			continue
		}
		filtered = append(filtered, f)
	}
	// runtime.Stack emits innermost-first; reverse so the chain reads
	// outer → inner, matching the direction of execution.
	for i, j := 0, len(filtered)-1; i < j; i, j = i+1, j-1 {
		filtered[i], filtered[j] = filtered[j], filtered[i]
	}
	return filtered
}

// parseGoStack parses the text format of runtime.Stack into frames.
// Format per frame is two lines:
//
//	github.com/org/pkg.(*T).Method(args…)
//	<TAB>/abs/path/file.go:123 +0x4a
func parseGoStack(s string) []StackFrame {
	lines := strings.Split(s, "\n")
	var out []StackFrame
	for i := 0; i+1 < len(lines); {
		funcLine := lines[i]
		if funcLine == "" || strings.HasPrefix(funcLine, "goroutine ") || strings.HasPrefix(funcLine, "created by ") {
			i++
			continue
		}
		fileLine := strings.TrimSpace(lines[i+1])
		if !strings.HasPrefix(lines[i+1], "\t") {
			i++
			continue
		}
		fn := funcLine
		if idx := strings.LastIndex(fn, "("); idx >= 0 {
			fn = fn[:idx]
		}
		if idx := strings.Index(fileLine, " "); idx >= 0 {
			fileLine = fileLine[:idx]
		}
		colon := strings.LastIndex(fileLine, ":")
		if colon < 0 {
			i += 2
			continue
		}
		line, _ := strconv.Atoi(fileLine[colon+1:])
		out = append(out, StackFrame{
			Function: strings.TrimSpace(fn),
			File:     fileLine[:colon],
			Line:     line,
		})
		i += 2
	}
	return out
}

// DumpTarget describes where to dump a trace.
type DumpTarget struct {
	// Dir is the output directory. If "", dumping is a no-op.
	Dir string
	// Expression is the string that is about to be passed to expr.Compile.
	Expression string
	// CallerFile / CallerLine identify the expr.Compile site. If 0/empty,
	// DumpD2 fills them from runtime.Caller.
	CallerFile string
	CallerLine int
	// Label is a short human-readable tag to include in filenames (e.g. "label-from").
	Label string
}

// DumpD2 writes a d2 diagram representing the map's entries with provenance,
// along with an edge to a node representing the expr.Compile site. Returns the
// path of the written file, or "" if dumping was disabled.
//
// If target.Dir is empty, uses ARGO_EXPR_TRACE_DIR; if that is also empty, it
// is a no-op and returns "". Safe on nil receiver.
func (m *Map) DumpD2(target DumpTarget) (string, error) {
	if m == nil {
		return "", nil
	}
	dir := target.Dir
	if dir == "" {
		dir = os.Getenv("ARGO_EXPR_TRACE_DIR")
	}
	if dir == "" {
		return "", nil
	}
	if target.CallerFile == "" {
		if _, file, line, ok := runtime.Caller(1); ok {
			target.CallerFile = file
			target.CallerLine = line
		}
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return "", err
	}

	content := m.RenderD2(target)
	name := filename(target)
	path := filepath.Join(dir, name)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return "", err
	}
	return path, nil
}

// RenderD2 returns the d2 source text without writing anything.
// Useful for tests and for callers that want to post-process the output.
func (m *Map) RenderD2(target DumpTarget) string {
	stack := captureStack()
	var b strings.Builder
	writeHeader(&b, target)
	writeEnvBlock(&b, m)
	writeCompileBlock(&b, target)
	writeStackBlock(&b, stack)
	writeEdges(&b, m, stack)
	return b.String()
}

func writeHeader(b *strings.Builder, t DumpTarget) {
	fmt.Fprintf(b, "# auto-generated by util/exprtrace — do not edit\n")
	fmt.Fprintf(b, "# expression : %s\n", oneLine(t.Expression))
	fmt.Fprintf(b, "# call site  : %s:%d\n", t.CallerFile, t.CallerLine)
	fmt.Fprintf(b, "# generated  : %s\n\n", time.Now().UTC().Format(time.RFC3339))
	b.WriteString("direction: right\n\n")
}

func writeEnvBlock(b *strings.Builder, m *Map) {
	b.WriteString("env: {\n")
	b.WriteString("  shape: package\n")
	fmt.Fprintf(b, "  label: \"env (%d keys)\"\n", m.Len())

	for _, k := range m.Keys() {
		e, _ := m.Entry(k)
		id := d2id(k)
		fmt.Fprintf(b, "  %s: {\n", id)
		fmt.Fprintf(b, "    label: %s\n", d2str(k))
		// d2 parses tooltips as markdown; escape < and > so strings like "<nil>"
		// and type literals don't trip its HTML element detector.
		fmt.Fprintf(b, "    tooltip: %s\n", d2str(escapeAngleBrackets(truncate(fmt.Sprint(e.Value), 256))))
		fmt.Fprintf(b, "    # set at %s:%d in %s\n", e.File, e.Line, e.Function)
		b.WriteString("  }\n")
	}
	b.WriteString("}\n\n")
}

func writeCompileBlock(b *strings.Builder, t DumpTarget) {
	b.WriteString("compile: {\n")
	b.WriteString("  shape: hexagon\n")
	fmt.Fprintf(b, "  label: %s\n", d2str(fmt.Sprintf("expr.Compile\\n%s:%d", shortenPath(t.CallerFile), t.CallerLine)))
	fmt.Fprintf(b, "  tooltip: %s\n", d2str(oneLine(t.Expression)))
	b.WriteString("}\n\n")

	b.WriteString("sources: {\n  shape: package\n  label: \"producers\"\n")
}

func writeStackBlock(b *strings.Builder, frames []StackFrame) {
	if len(frames) == 0 {
		return
	}
	b.WriteString("stack: {\n")
	b.WriteString("  shape: package\n")
	fmt.Fprintf(b, "  label: \"call chain (%d frames)\"\n", len(frames))
	b.WriteString("  direction: down\n")
	for i, f := range frames {
		id := fmt.Sprintf("f%d", i)
		label := fmt.Sprintf("%s\\n%s:%d", shortFn(f.Function), shortenPath(f.File), f.Line)
		fmt.Fprintf(b, "  %s: {\n", id)
		fmt.Fprintf(b, "    label: %s\n", d2str(label))
		b.WriteString("    shape: rectangle\n")
		b.WriteString("  }\n")
	}
	for i := 0; i < len(frames)-1; i++ {
		fmt.Fprintf(b, "  f%d -> f%d: calls\n", i, i+1)
	}
	b.WriteString("}\n\n")
}

func writeEdges(b *strings.Builder, m *Map, stack []StackFrame) {
	// Group keys by (file, line, function) so we can draw one source per
	// site rather than one per key.
	type site struct {
		file string
		line int
		fn   string
	}
	groups := map[site][]string{}
	for _, k := range m.Keys() {
		e, _ := m.Entry(k)
		s := site{file: e.File, line: e.Line, fn: e.Function}
		groups[s] = append(groups[s], k)
	}

	siteIDs := map[site]string{}
	i := 0
	keysBySite := make([]site, 0, len(groups))
	for s := range groups {
		keysBySite = append(keysBySite, s)
	}
	sort.Slice(keysBySite, func(a, b int) bool {
		if keysBySite[a].file != keysBySite[b].file {
			return keysBySite[a].file < keysBySite[b].file
		}
		return keysBySite[a].line < keysBySite[b].line
	})
	for _, s := range keysBySite {
		id := fmt.Sprintf("src_%d", i)
		i++
		siteIDs[s] = id
		label := fmt.Sprintf("%s:%d\\n%s", shortenPath(s.file), s.line, shortFn(s.fn))
		fmt.Fprintf(b, "  %s: {\n", id)
		fmt.Fprintf(b, "    label: %s\n", d2str(label))
		b.WriteString("    shape: rectangle\n")
		b.WriteString("  }\n")
	}
	b.WriteString("}\n\n")

	// Edges: each source -> each key it produced; env -> compile.
	for _, s := range keysBySite {
		srcID := siteIDs[s]
		for _, k := range groups[s] {
			fmt.Fprintf(b, "sources.%s -> env.%s: set\n", srcID, d2id(k))
		}
	}
	b.WriteString("env -> compile: evaluated\n")

	// Connect the innermost stack frame (the one that called DumpD2)
	// to the compile node so the call chain visually terminates at compile.
	if len(stack) > 0 {
		fmt.Fprintf(b, "stack.f%d -> compile: enters\n", len(stack)-1)
	}
}

// d2id turns an arbitrary string key into a safe d2 identifier. We quote it so
// dots and special characters are preserved exactly.
func d2id(key string) string {
	// Use quoted identifier form so the text is preserved verbatim.
	return d2str(key)
}

// d2str renders a string as a double-quoted d2 literal.
func d2str(s string) string {
	var b strings.Builder
	b.WriteByte('"')
	for _, r := range s {
		switch r {
		case '\\':
			b.WriteString("\\\\")
		case '"':
			b.WriteString("\\\"")
		case '\n':
			b.WriteString("\\n")
		default:
			b.WriteRune(r)
		}
	}
	b.WriteByte('"')
	return b.String()
}

func oneLine(s string) string {
	return strings.Join(strings.Fields(s), " ")
}

func escapeAngleBrackets(s string) string {
	s = strings.ReplaceAll(s, "<", "&lt;")
	s = strings.ReplaceAll(s, ">", "&gt;")
	return s
}

func truncate(s string, n int) string {
	if len(s) <= n {
		return s
	}
	return s[:n] + "…"
}

func shortenPath(p string) string {
	// Keep only the last two path components.
	parts := strings.Split(filepath.ToSlash(p), "/")
	if len(parts) <= 2 {
		return p
	}
	return strings.Join(parts[len(parts)-2:], "/")
}

func shortFn(fn string) string {
	// github.com/argoproj/argo-workflows/v4/workflow/controller.(*wfOperationCtx).setGlobalParameters
	// → controller.(*wfOperationCtx).setGlobalParameters
	if idx := strings.LastIndex(fn, "/"); idx >= 0 {
		return fn[idx+1:]
	}
	return fn
}

func filename(t DumpTarget) string {
	ts := time.Now().UTC().Format("20060102T150405.000")
	h := sha1.Sum([]byte(t.Expression))
	short := hex.EncodeToString(h[:4])
	label := t.Label
	if label == "" {
		label = "expr"
	}
	label = strings.Map(func(r rune) rune {
		if r >= 'a' && r <= 'z' || r >= 'A' && r <= 'Z' || r >= '0' && r <= '9' || r == '-' || r == '_' {
			return r
		}
		return '-'
	}, label)
	return fmt.Sprintf("%s-%s-%s.d2", ts, label, short)
}
